    private void downloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downloadButtonActionPerformed
        
    fileChooser = new JFileChooser();       
     // Connect to the database and retrieve the blob data
     
     StatementController stcon = new StatementController();
     try (ResultSet rs = stcon.selectData("SELECT data,name FROM zip_files WHERE id ='"+dId+"'");) {
         rs.next();
         InputStream blobStream = rs.getBinaryStream(1);
         datapackName=rs.getString(2);

         fileChooser.setSelectedFile(new File(datapackName)); // set default file name
         int userSelection = fileChooser.showSaveDialog(this);
         if (userSelection == JFileChooser.APPROVE_OPTION) {
             File fileToSave=fileChooser.getSelectedFile();
             if (fileToSave != null) {
                 // write your code to save the file with the chosen name and location

                 // Create a ZipOutputStream to compress the blob data
                 try (ZipOutputStream zipOut = new ZipOutputStream(new FileOutputStream(fileToSave))) {
                     ZipEntry zipEntry = new ZipEntry(datapackName);
                     zipOut.putNextEntry(zipEntry);

                     // Write the compressed blob data to the ZipOutputStream
                     byte[] buffer = new byte[1024];
                     int len;
                     while ((len = blobStream.read(buffer)) > 0) {
                         zipOut.write(buffer, 0, len);
                     }

                     // Close the ZipOutputStream
                     zipOut.closeEntry();
                 }
                 JOptionPane.showMessageDialog(null, "File Downloaded");
             }
            }
         
     } catch (SQLException | IOException e) {
         JOptionPane.showMessageDialog(null, e.getMessage());
     } 
     stcon.selectCloseCon();
    }//GEN-LAST:event_downloadButtonActionPerformed

